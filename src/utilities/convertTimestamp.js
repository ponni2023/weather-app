export const convertTimestamp = (timestamp) => {
    var d = new Date(timestamp * 1000), // Convert the passed timestamp to milliseconds
        hh = d.getHours(),
        h = hh,
        min = ('0' + d.getMinutes()).slice(-2),     // Add leading 0.
        ampm = 'AM',
        time;

    if (hh > 12) {
        h = hh - 12;
        ampm = 'PM';
    } else if (hh === 12) {
        h = 12;
        ampm = 'PM';
    } else if (hh === 0) {
        h = 12;
    }
    time =  h + ':' + min + ' ' + ampm;
    return time;
}